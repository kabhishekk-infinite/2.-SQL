--10.
SELECT D.DNAME, D.DEPTNO, SUM(E.SAL) AS 'Total Salary'
FROM DEPT D
LEFT JOIN EMP E ON D.DEPTNO = E.DEPTNO
GROUP BY D.DNAME, D.DEPTNO;

